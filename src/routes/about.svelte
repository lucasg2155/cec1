<h1>Nuestra Misión</h1>

<p>Con casi 25 años de trayectoria somos el primer gremio no estatal de la Comunidad. Una gremio de servicio y gestión que desde su origen se ha caracterizado por su carácter pionero, por ser los primeros en ofrecer farmacia, clinica y odontologia. Por ser los primeros en ofrecer dobles beneficios. Por ser de los primeros en integrar la figura del tutor en la obra social. Por ser los primeros en ofrecer becas de desarrollo profesional durante la carrera. Por ser los primeros en poner en marcha otra forma de entender qué es ser un trabajador.</p>